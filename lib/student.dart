class Student {
  String name;
  String studentid;
  String image;
  String email;
  String dateOfBirth;
  String phoneNumber;
  String aboutMe;
  String socialMediaLink;

  Student({
    required this.name,
    required this.studentid,
    required this.image,
    required this.email,
    required this.dateOfBirth,
    required this.phoneNumber,
    required this.aboutMe,
    required this.socialMediaLink,
  });
}